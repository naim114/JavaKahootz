    private void BtnConfirmSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnConfirmSignUpActionPerformed
        String name = TxtUsername.getText();
        String pwd = new String(TxtPassword.getPassword());
        String pwd2 = new String(TxtPassword2.getPassword());

        int reply = JOptionPane.showConfirmDialog(null, "Are you sure you want to confirm this changes?");

        if (reply == JOptionPane.YES_OPTION) {
            try {
                // check if there is empty field
                if (name.equals("") || pwd.equals("") || pwd2.equals("")) {
                    throw new Exception("Please enter all field.");
                }

                // check if password and re-enter password isn't the same
                if (!pwd.equals(pwd2)) {
                    throw new Exception("Password and Re-enter password is not same.");
                }

                // check if username already taken
                if (isUsernameDuplicateExcept(name, this.user.username)) {
                    throw new Exception("Username taken. Please enter different username.");
                }

                Users users = new Users();
                JSONArray arr = users.getJSONArray();
                int selected_user_index = 0;

                for (int i = 0; i < users.size(); i++) {
                    User checking_user = users.getUserByIndex(i);

                    if (checking_user.username.equals(this.user.username)) {
                        selected_user_index = i;
                    }
                }
                arr.remove(selected_user_index);

                JSONObject userJSON = new JSONObject();
                userJSON.put("username", name);
                userJSON.put("password", pwd);

                arr.add(userJSON);

                // clear text file
                FileWriter fwOb = new FileWriter("tb_user.txt", false);
                PrintWriter pwOb = new PrintWriter(fwOb, false);
                pwOb.flush();
                pwOb.close();
                fwOb.close();

                // write back all of users with new user added
                FileWriter writer = new FileWriter("tb_user.txt", true);
                writer.write(arr.toJSONString());

                writer.close();

                // close window
                this.dispose();
                new ManageUser().setVisible(true);
                JOptionPane.showMessageDialog(null, "Success editing user " + this.user.username);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_BtnConfirmSignUpActionPerformed

