    public static boolean isUsernameDuplicateExcept(String check_username, String original_username) {
        try {
            JSONParser parser = new JSONParser();

            Users users = new Users();

            for (int i = 0; i < users.size(); i++) {
                User user = users.get(i);

                if (check_username.equals(user.username) && !check_username.equals(original_username)) {
                    return true;
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        return false;
    }
